-- 7) Funzione SQL per la dashboard statistiche
CREATE OR REPLACE FUNCTION public.statistiche_catalogo()
RETURNS jsonb
LANGUAGE sql
STABLE
SECURITY INVOKER
SET search_path = public, extensions
AS $$
  SELECT jsonb_build_object(
    'totale', (SELECT COUNT(*) FROM catalogo),
    'da_verificare', (SELECT COUNT(*) FROM catalogo WHERE da_verificare),
    'con_note', (SELECT COUNT(*) FROM catalogo WHERE note IS NOT NULL),
    'completezza', jsonb_build_object(
      'autore',                  (SELECT COUNT(*) FROM catalogo WHERE autore IS NOT NULL),
      'titolo',                  (SELECT COUNT(*) FROM catalogo WHERE titolo IS NOT NULL),
      'specifica',               (SELECT COUNT(*) FROM catalogo WHERE specifica IS NOT NULL),
      'partitura_parti_formato', (SELECT COUNT(*) FROM catalogo WHERE partitura_parti_formato IS NOT NULL),
      'antica_collocazione',     (SELECT COUNT(*) FROM catalogo WHERE antica_collocazione IS NOT NULL),
      'casa_editrice',           (SELECT COUNT(*) FROM catalogo WHERE casa_editrice IS NOT NULL),
      'anno',                    (SELECT COUNT(*) FROM catalogo WHERE anno IS NOT NULL),
      'pagine',                  (SELECT COUNT(*) FROM catalogo WHERE pagine IS NOT NULL),
      'data_ingresso',           (SELECT COUNT(*) FROM catalogo WHERE data_ingresso IS NOT NULL),
      'revisore',                (SELECT COUNT(*) FROM catalogo WHERE revisore IS NOT NULL),
      'provenienza',             (SELECT COUNT(*) FROM catalogo WHERE provenienza IS NOT NULL),
      'posizione_led',           (SELECT COUNT(*) FROM catalogo WHERE posizione_led IS NOT NULL)
    ),
    'per_secolo', (
      SELECT jsonb_object_agg(secolo, n) FROM (
        SELECT
          CASE
            WHEN anno_num BETWEEN 1500 AND 1699 THEN 'XVI-XVII'
            WHEN anno_num BETWEEN 1700 AND 1799 THEN 'XVIII'
            WHEN anno_num BETWEEN 1800 AND 1899 THEN 'XIX'
            WHEN anno_num BETWEEN 1900 AND 1999 THEN 'XX'
            WHEN anno_num >= 2000 THEN 'XXI'
            WHEN anno IS NOT NULL THEN 'Non parsabile'
            ELSE 'Assente'
          END AS secolo,
          COUNT(*) AS n
        FROM catalogo
        GROUP BY 1
      ) t
    ),
    'per_decennio', (
      SELECT jsonb_agg(jsonb_build_object('decennio', decennio, 'n', n) ORDER BY decennio) FROM (
        SELECT (anno_num / 10) * 10 AS decennio, COUNT(*) AS n
        FROM catalogo WHERE anno_num BETWEEN 1700 AND 2030
        GROUP BY 1 ORDER BY 1
      ) t
    ),
    'case_editrici_top', (
      SELECT jsonb_agg(jsonb_build_object('valore', label, 'conteggio', n) ORDER BY n DESC) FROM (
        SELECT INITCAP(LOWER(casa_editrice)) AS label, COUNT(*) AS n
        FROM catalogo WHERE casa_editrice IS NOT NULL
        GROUP BY LOWER(casa_editrice), INITCAP(LOWER(casa_editrice))
        ORDER BY n DESC LIMIT 15
      ) t
    ),
    'strumenti_top', (
      SELECT jsonb_agg(jsonb_build_object('valore', label, 'conteggio', n) ORDER BY n DESC) FROM (
        SELECT INITCAP(LOWER(specifica)) AS label, COUNT(*) AS n
        FROM catalogo WHERE specifica IS NOT NULL
        GROUP BY LOWER(specifica), INITCAP(LOWER(specifica))
        ORDER BY n DESC LIMIT 15
      ) t
    )
  );
$$;

GRANT EXECUTE ON FUNCTION public.statistiche_catalogo() TO anon, authenticated;
